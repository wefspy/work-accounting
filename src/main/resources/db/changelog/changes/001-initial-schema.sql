    version            BIGINT    DEFAULT 0                     NOT NULL,

    CONSTRAINT pk_team_membership PRIMARY KEY (team_membership_id),
    CONSTRAINT fk_tm_team FOREIGN KEY (team_id) REFERENCES teams (team_id),
