CREATE TABLE users
(
    user_id       BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    email         VARCHAR(255)                            NOT NULL,
    password_hash VARCHAR(255)                            NOT NULL,
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP     NOT NULL,
    is_deleted    BOOLEAN   DEFAULT FALSE                 NOT NULL,
    version       BIGINT    DEFAULT 0                     NOT NULL,

    CONSTRAINT pk_users PRIMARY KEY (user_id)
);

CREATE UNIQUE INDEX idx_users_email_uniq_active
    ON users (email)
    WHERE is_deleted = false;

CREATE TABLE roles
(
    role_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name    VARCHAR(255)                            NOT NULL,
    version BIGINT DEFAULT 0                        NOT NULL,

    CONSTRAINT pk_roles PRIMARY KEY (role_id),
    CONSTRAINT uc_roles_name UNIQUE (name)
);

CREATE TABLE users_roles
(
    user_id BIGINT NOT NULL,
    role_id BIGINT NOT NULL,

    CONSTRAINT pk_users_roles PRIMARY KEY (user_id, role_id),
    CONSTRAINT fk_users_roles_user FOREIGN KEY (user_id) REFERENCES users (user_id),
    CONSTRAINT fk_users_roles_role FOREIGN KEY (role_id) REFERENCES roles (role_id)
);

CREATE TABLE users_info
(
    user_id     BIGINT           NOT NULL,
    last_name   VARCHAR(255)     NOT NULL,
    first_name  VARCHAR(255)     NOT NULL,
    middle_name VARCHAR(255),
    version     BIGINT DEFAULT 0 NOT NULL,

    CONSTRAINT pk_users_info PRIMARY KEY (user_id),
    CONSTRAINT fk_users_info_user FOREIGN KEY (user_id) REFERENCES users (user_id) ON DELETE CASCADE
);

CREATE TABLE participants
(
    participant_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_by     BIGINT                                  NOT NULL,
    last_name      VARCHAR(255)                            NOT NULL,
    first_name     VARCHAR(255)                            NOT NULL,
    middle_name    VARCHAR(255),
    bio            TEXT,
    telegram       VARCHAR(255),
    created_at     TIMESTAMP DEFAULT CURRENT_TIMESTAMP     NOT NULL,
    version        BIGINT    DEFAULT 0                     NOT NULL,

    CONSTRAINT pk_participants PRIMARY KEY (participant_id),
    CONSTRAINT fk_participants_created_by FOREIGN KEY (created_by) REFERENCES users_info (user_id)
);

CREATE TABLE semesters
(
    semester_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name        TEXT                                    NOT NULL,
    starts_at   TIMESTAMP                               NOT NULL,
    ends_at     TIMESTAMP                               NOT NULL,
    version     BIGINT DEFAULT 0                        NOT NULL,

    CONSTRAINT pk_semesters PRIMARY KEY (semester_id),
    CONSTRAINT uc_semesters_name UNIQUE (name)
);

CREATE TABLE project_status
(
    project_status_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name              TEXT,
    version           BIGINT DEFAULT 0                        NOT NULL,

    CONSTRAINT pk_project_status PRIMARY KEY (project_status_id),
    CONSTRAINT uc_project_status_name UNIQUE (name)
);

CREATE TABLE projects
(
    project_id        BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_by        BIGINT                                  NOT NULL,
    project_status_id BIGINT                                  NOT NULL,
    title             VARCHAR(255)                            NOT NULL,
    description       TEXT,
    tech_stack        VARCHAR(255),
    team_size         INTEGER,
    version           BIGINT DEFAULT 0                        NOT NULL,

    CONSTRAINT pk_projects PRIMARY KEY (project_id),
    CONSTRAINT fk_projects_created_by FOREIGN KEY (created_by) REFERENCES users_info (user_id),
    CONSTRAINT fk_projects_status FOREIGN KEY (project_status_id) REFERENCES project_status (project_status_id)
);

CREATE TABLE project_status_history
(
    project_status_history_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    project_id                BIGINT                                  NOT NULL,
    from_status_id            BIGINT                                  NOT NULL,
    to_status_id              BIGINT                                  NOT NULL,
    changed_by                BIGINT                                  NOT NULL,
    changed_at                TIMESTAMP DEFAULT CURRENT_TIMESTAMP     NOT NULL,
    version                   BIGINT    DEFAULT 0                     NOT NULL,

    CONSTRAINT pk_project_status_history PRIMARY KEY (project_status_history_id),
    CONSTRAINT fk_psh_project FOREIGN KEY (project_id) REFERENCES projects (project_id),
    CONSTRAINT fk_psh_from_status FOREIGN KEY (from_status_id) REFERENCES project_status (project_status_id),
    CONSTRAINT fk_psh_to_status FOREIGN KEY (to_status_id) REFERENCES project_status (project_status_id),
    CONSTRAINT fk_psh_changed_by FOREIGN KEY (changed_by) REFERENCES users_info (user_id)
);

CREATE TABLE project_revisions
